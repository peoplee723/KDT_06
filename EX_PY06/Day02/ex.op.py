# 연산자
# 
# 1. 산술연산자
# - 종류 : +, -, *, /, //, %, **
num1=8
num2=3
#출력형태 : 8 + 3 = 11
print(F'{num1} + {num2} = {num1+num2}')
print(F'{num1} - {num2} = {num1-num2}')
print(F'{num1} * {num2} = {num1*num2}')
print(F'{num1} / {num2} = {num1/num2}')
print(F'{num1} // {num2} = {num1//num2}')
print(F'{num1} % {num2} = {num1%num2}')
print(F'{num1} ** {num2} = {num1**num2}')

# 비교연산자
# - 종류: >, <, >=, <=, ==, !=
num1= 'aF'  #컴퓨터는 문자로 숫자로 보기 때문에 비교연산 가능 
num2= 'ac'
print(F'{num1} > {num2} = {num1>num2}')
print(F'{num1} < {num2} = {num1<num2}')
print(F'{num1} >= {num2} = {num1>=num2}')
print(F'{num1} <= {num2} = {num1<=num2}')
print(F'{num1} == {num2} = {num1==num2}')
print(F'{num1} != {num2} = {num1!=num2}')

